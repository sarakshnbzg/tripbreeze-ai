def evaluate_with_llm_judge(
    rows: list[dict[str, Any]],
    *,
    provider: str,
    model: str,
) -> dict[str, Any]:
    """Score generated answers with an LLM judge and return row-level plus aggregate results."""
    llm = create_chat_model(provider, model, temperature=0)
    judged_rows: list[dict[str, Any]] = []
    total_scores = {
        "faithfulness_to_context": 0,
        "answer_correctness": 0,
        "answer_completeness": 0,
        "groundedness": 0,
        "overall_score": 0,
    }
    passed = 0

    for row in rows:
        contexts = row.get("retrieved_contexts") or []
        prompt = LLM_JUDGE_PROMPT.format(
            question=str(row.get("user_input", "")).strip(),
            reference=str(row.get("reference", "")).strip(),
            contexts="\n\n".join(f"[{idx}] {context}" for idx, context in enumerate(contexts, start=1)) or "None",
            response=str(row.get("response", "")).strip() or "No answer generated.",
        )
        response = invoke_with_retry(llm, prompt)
        raw_content = getattr(response, "content", "")
        parsed = _extract_json_object(raw_content if isinstance(raw_content, str) else str(raw_content))
        judgement = _normalise_judge_result(parsed)
        judged_rows.append({**row, "judge": judgement})

        for key in total_scores:
            total_scores[key] += judgement[key]
        if judgement["pass"]:
            passed += 1

    count = len(judged_rows) or 1
    averages = {key: round(value / count, 3) for key, value in total_scores.items()}
    return {
        "judge_provider": provider,
        "judge_model": model,
        "sample_count": len(judged_rows),
        "pass_rate": round(passed / count, 3),
        "average_scores": averages,
        "rows": judged_rows,
    }


def evaluate_itinerary_with_llm_judge(
    *,
    input_state: dict[str, Any],
    final_itinerary: str,
    itinerary_data: dict[str, Any],
    provider: str,
    model: str,
) -> dict[str, Any]:
    """Judge a generated itinerary for quality, relevance, and constraint following."""
    llm = create_chat_model(provider, model, temperature=0)
    prompt = ITINERARY_JUDGE_PROMPT.format(
        trip_request=json.dumps(input_state.get("trip_request", {}), indent=2, ensure_ascii=True),
        user_feedback=str(input_state.get("user_feedback", "") or "None"),
        destination_info=str(input_state.get("destination_info", "") or "None"),
        budget=json.dumps(input_state.get("budget", {}), indent=2, ensure_ascii=True),
        attraction_candidates=json.dumps(
            input_state.get("attraction_candidates", []),
            indent=2,
            ensure_ascii=True,
        ),
        itinerary_data=json.dumps(itinerary_data, indent=2, ensure_ascii=True),
        itinerary_markdown=final_itinerary or "No itinerary generated.",
    )
    response = invoke_with_retry(llm, prompt)
    raw_content = getattr(response, "content", "")
    parsed = _extract_json_object(raw_content if isinstance(raw_content, str) else str(raw_content))
    result = _normalise_itinerary_judge_result(parsed)
    return {
        "judge_provider": provider,
        "judge_model": model,
        "result": result,
    }
