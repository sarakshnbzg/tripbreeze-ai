LLM_JUDGE_PROMPT = """You are an impartial evaluator for a travel assistant's grounded answer.
Score the answer against the provided question, reference answer, and retrieved context.

Return JSON only with this exact schema:
{{
  "faithfulness_to_context": 1-5,
  "answer_correctness": 1-5,
  "answer_completeness": 1-5,
  "groundedness": 1-5,
  "overall_score": 1-5,
  "pass": true,
  "strengths": ["short point"],
  "issues": ["short point"],
  "rationale": "1-3 short sentences"
}}

Scoring rubric:
- 5: excellent
- 4: good with minor issues
- 3: mixed / acceptable but notable gaps
- 2: poor
- 1: unusable or incorrect

Guidelines:
- Faithfulness to context: does the answer stay consistent with the retrieved context?
- Answer correctness: is the answer correct relative to the reference and context?
- Answer completeness: does it cover the important parts of the question?
- Groundedness: does it avoid unsupported claims beyond the retrieved context?
- pass should be true only if the answer is broadly correct, grounded, and useful.

Question:
{question}

Reference answer:
{reference}

Retrieved context:
{contexts}

Assistant answer:
{response}
"""

